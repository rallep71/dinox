def update_po_file(file_path, lang_code):
    with open(file_path, 'r', encoding='utf-8') as f:
        lines = f.readlines()

    translations = TRANSLATIONS.get(lang_code, TRANSLATIONS.get("en", {}))
    
    if lang_code not in TRANSLATIONS:
        if lang_code == "en":
            translations = {k: k for k in STRINGS}
        else:
            translations = {k: k for k in STRINGS}

    new_lines = []
    i = 0
    while i < len(lines):
        line = lines[i]
        
        # Check if this line is a msgid we want to translate
        match = re.match(r'^msgid "(.*)"\s*$', line)
        if match:
            current_msgid = match.group(1)
            if current_msgid in translations:
                # Found a target msgid
                
                # 1. Check for fuzzy flag in previous lines (comments)
                # We need to look back from the *current* position in new_lines (which represents the processed file so far)
                # But wait, we are appending to new_lines.
                # We can check the last few elements of new_lines.
                
                # Scan backwards in new_lines to find comments
                k = len(new_lines) - 1
                while k >= 0 and (new_lines[k].startswith("#") or new_lines[k].strip() == ""):
                    if "#, fuzzy" in new_lines[k]:
                        # Remove the fuzzy flag
                        new_lines[k] = new_lines[k].replace("#, fuzzy", "#").replace("#, ,", "#,")
                        # Clean up empty commas or just "#" if it's empty now
                        if new_lines[k].strip() == "#":
                            new_lines[k] = "" # Remove empty comment line? Or keep it? standard is usually just removing the word fuzzy.
                            # If the line becomes just "#", it's fine or we can remove it.
                            # Let's just replace "#, fuzzy" with "" if it's the only thing, or remove it from the list.
                            pass
                    k -= 1
                
                # 2. Add the msgid line
                new_lines.append(line)
                
                # 3. Handle the msgstr line(s)
                # The next line should be msgstr
                if i + 1 < len(lines) and lines[i+1].startswith("msgstr"):
                    # We replace this line with our translation
                    new_translation = translations[current_msgid]
                    new_lines.append(f'msgstr "{new_translation}"\n')
                    i += 2 # Skip msgid and msgstr
                    continue
                else:
                    # Should not happen in valid PO, but just in case
                    pass
        
        new_lines.append(line)
        i += 1

    # Write back
    with open(file_path, 'w', encoding='utf-8') as f:
        f.writelines(new_lines)
    print(f"Updated {lang_code}")

